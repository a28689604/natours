tourSchema.pre(/^find/, function (next) {
  this.find({ secretTour: { $ne: true } });

  this.start = Date.now();
  next();
});

tourSchema.pre(/^find/, function (next) {
  // IMPORTANT populate is going to add some extra query
  this.populate({
    path: 'guides',
    // IMPORTANT get rid of the fields we don't want
    select: '-__v -passwordChangedAt',
  });
  next();
});

